
import math

print("Квадратный корень из 25:", math.sqrt(25))
print("2 в степени 8:", math.pow(2, 8))

print("Округление вверх:", math.ceil(4.3))
print("Округление вниз:", math.floor(4.7))

print("Натуральный логарифм числа e:", math.log(math.e))
print("Логарифм по основанию 10:", math.log10(1000))

angle = 60
print("Синус 60°:", math.sin(math.radians(angle)))
print("Косинус 60°:", math.cos(math.radians(angle)))
print("Тангенс 60°:", math.tan(math.radians(angle)))

# Константы
print("Число Пи:", math.pi)
print("Число e:", math.e)
